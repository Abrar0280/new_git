number=int(input("Enter a Number from 1-8: "))

if number == 1:
    outputChar = '!'

elif number == 2:
    outputChar = '?'

elif number == 3:
    outputChar = ','

elif number == 4:
    outputChar = '.'

elif number == 5:
    outputChar = ' '

elif number == 6:
    outputChar = ';'

elif number == 7:
    outputChar = '"'

elif number == 8:
    outputChar = '/'

print("Equivalent symbol: {}".format(outputChar))

# === Output === #

# Enter a Number from 1-8: 8
# Equivalent symbol: /

